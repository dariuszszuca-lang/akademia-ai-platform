export const iconKeys = Object.keys(iconRegistry);

export function Icon({
  name,
  fallback,
  ...props
}: IconProps & { name: string; fallback?: string }) {
  const Comp = iconRegistry[name];
  if (Comp) return <Comp {...props} />;
  // Fallback: render as text (legacy emoji)
  return <span className="text-lg leading-none">{fallback ?? name}</span>;
}

export function isKnownIcon(name: string) {
  return Boolean(iconRegistry[name]);
}
